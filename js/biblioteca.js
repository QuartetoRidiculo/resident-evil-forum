const background = document.getElementById("background");
const imagebraba = document.createElement("img");

listaJogos.forEach((jogo) => {
  const elementoDiv = document.createElement("div");
  elementoDiv.classList.add("divCard");

  const divNomeNota = document.createElement("div");
  divNomeNota.classList.add("divNomeNota");

  elementoDiv.appendChild(divNomeNota);

  //------------------------------------------------------------------------

  const img = document.createElement("img");
  img.src = jogo.imagemJogo;
  img.classList.add("img");
  elementoDiv.appendChild(img);

  const nome = document.createElement("h3");
  nome.textContent = jogo.nomeJogo;
  nome.classList.add("nome");
  divNomeNota.appendChild(nome);

  const nota = document.createElement("p");
  nota.textContent = jogo.notaMetacritic;
  nota.classList.add("nota");
  divNomeNota.appendChild(nota);

  if (jogo.notaMetacritic >= 75) {
    nota.style.backgroundColor = "#00ce7a";
  } else if (jogo.notaMetacritic >= 50) {
    nota.style.backgroundColor = "#ffbd3f";
  } else {
    nota.style.backgroundColor = "#ff6874";
  }

  const data = document.createElement("p");
  data.textContent = jogo.dataJogo;
  data.classList.add("data");
  elementoDiv.appendChild(data);

  const miniSinopse = document.createElement("p");
  miniSinopse.textContent = jogo.miniSinopse;
  miniSinopse.classList.add("miniSinopse");
  elementoDiv.appendChild(miniSinopse);

  document.getElementById("listaJogos").appendChild(elementoDiv);

  elementoDiv.addEventListener("click", () => {
    const modal = document.createElement("div");
    modal.classList.add("divModal");
    const contentModal = document.createElement("div");
    contentModal.classList.add("contModal");
    const modalNomeNota = document.createElement("div");
    modalNomeNota.classList.add("modalNomeNota");
    const modalTextos = document.createElement("div");

    const modalImg = document.createElement("img");
    modalImg.src = jogo.imagemModal;
    modalImg.classList.add("modalImg")

    const mCriticLogo = document.createElement("img");
    mCriticLogo.src = "../images/metacritic_logo.png";
    mCriticLogo.classList.add("logoM")

    const modalNome = document.createElement("h1");
    modalNome.textContent = jogo.nomeJogo;
    modalNome.classList.add("modalNome");

    const modalNota = document.createElement("p");
    modalNota.textContent = jogo.notaMetacritic;

    if (jogo.notaMetacritic >= 75) {
      modalNota.style.backgroundColor = "#00ce7a";
    } else if (jogo.notaMetacritic >= 50) {
      modalNota.style.backgroundColor = "#ffbd3f";
    } else {
      modalNota.style.backgroundColor = "#ff6874";
    }
    modalNota.classList.add("nota");

    const modalData = document.createElement("h2");
    modalData.textContent = jogo.dataJogo;
    modalData.classList.add("modalData");

    const modalSinopse = document.createElement("p");
    modalSinopse.textContent = "Sinopse:\n" + jogo.sinopse;
    modalSinopse.classList.add("modalSinopse");

    modal.appendChild(contentModal);
    contentModal.appendChild(modalImg);
    modalNomeNota.appendChild(modalNome);
    contentModal.appendChild(modalNomeNota)
    modalNomeNota.appendChild(mCriticLogo);
    modalNomeNota.appendChild(modalNota);
    modalTextos.appendChild(modalData);
    contentModal.appendChild(modalData);
    contentModal.appendChild(modalSinopse);
    background.appendChild(modal);

    modal.addEventListener("click", (e) => {
      if (e.target === modal) {
        modal.remove();
      }
    });
  });
});
